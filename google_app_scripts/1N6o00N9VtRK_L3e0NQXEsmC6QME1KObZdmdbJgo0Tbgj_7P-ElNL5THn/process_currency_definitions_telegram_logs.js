/**
 * Sorts the Currencies tab A→Z by column A (Currency name), skipping the
 * header row. Called after every insert so the new SKU lands in the right
 * alphabetical position and is immediately findable.
 */
function sortCurrenciesAZ() {
  try {
    const spreadsheet = SpreadsheetApp.openByUrl(MAIN_LEDGER_URL);
    const sheet = spreadsheet.getSheetByName(CURRENCIES_SHEET_NAME);
    if (!sheet) {
      Logger.log('ERROR: Currencies sheet not found for sort');
      return;
    }

    const lastRow = sheet.getLastRow();
    if (lastRow <= 1) return; // Header only or empty

    // Sort range A2:L{lastRow} by column A ascending
    const range = sheet.getRange(2, 1, lastRow - 1, 12);
    range.sort({ column: 1, ascending: true });
    Logger.log('Sorted Currencies tab A→Z');
  } catch (e) {
    Logger.log('Error sorting Currencies tab: ' + e.message);
  }
}

