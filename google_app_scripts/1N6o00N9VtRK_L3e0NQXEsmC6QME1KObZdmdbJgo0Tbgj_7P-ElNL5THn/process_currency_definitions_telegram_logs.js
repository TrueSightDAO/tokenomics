/** Sheet name on the Main Ledger where currency definitions land. */
const CURRENCIES_SHEET_NAME = 'Currencies';

/** Main Ledger spreadsheet (same as CONTRIBUTORS_SHEET_URL). */
const MAIN_LEDGER_URL = 'https://docs.google.com/spreadsheets/d/1GE7PUq-UT6x2rBN-Q2ksogbWpgyuh2SaxJyG_uEK6PU/edit';

/** Telegram Chat Logs workbook. */
