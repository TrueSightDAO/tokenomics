// ============================================================================

/**
 * Check if a currency definition record already exists in the Currencies tab
 * by matching the currency name (column A). Returns true if found.
 */
function currencyDefinitionRecordExists(currencyName) {
  try {
    const spreadsheet = SpreadsheetApp.openByUrl(MAIN_LEDGER_URL);
    const sheet = spreadsheet.getSheetByName(CURRENCIES_SHEET_NAME);
    if (!sheet) return false;

    const data = sheet.getDataRange().getValues();
    const headerRow = 0; // Row 1 (0-based index 0) is the header

    for (let i = headerRow + 1; i < data.length; i++) {
      const row = data[i];
      const existingName = row[0] ? String(row[0]).trim().toLowerCase() : '';
      if (existingName === currencyName.trim().toLowerCase()) {
        Logger.log('Currency definition already exists: ' + currencyName);
        return true;
      }
    }
    return false;
  } catch (e) {
    Logger.log('Error checking for existing currency definition: ' + e.message);
    return false;
  }
}

