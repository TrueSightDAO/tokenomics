/**
 * Appends a new row to the Currencies tab with the parsed fields.
 * Columns A–L map to:
 *   A = Currency
 *   B = Price in USD
 *   C = Serializable
 *   D = Product Image
 *   E = Landing Page
 *   F = Ledger
 *   G = Farm Name
 *   H = State
 *   I = Country
 *   J = Year
 *   K = Unit Weight (grams)
 *   L = Unit Weight (ounces)
 *
 * @returns {boolean} true if the row was inserted
 */
function insertCurrencyDefinitionRecord(details) {
  try {
    const spreadsheet = SpreadsheetApp.openByUrl(MAIN_LEDGER_URL);
    const sheet = spreadsheet.getSheetByName(CURRENCIES_SHEET_NAME);
    if (!sheet) {
      Logger.log('ERROR: Currencies sheet not found');
      return false;
    }

    const newRow = [
      details.currency || '',
      details.priceInUsd || '',
      details.serializable || 'TRUE',
      details.productImage || '',
      details.landingPage || '',
      details.ledger || '',
      details.farmName || '',
      details.state || '',
      details.country || '',
      details.year || '',
      details.unitWeightGrams || '',
      details.unitWeightOunces || ''
    ];

    sheet.appendRow(newRow);
    Logger.log('Inserted currency definition: ' + (details.currency || '(unknown)'));
    return true;
  } catch (e) {
    Logger.log('Error inserting currency definition record: ' + e.message);
    return false;
  }
}

