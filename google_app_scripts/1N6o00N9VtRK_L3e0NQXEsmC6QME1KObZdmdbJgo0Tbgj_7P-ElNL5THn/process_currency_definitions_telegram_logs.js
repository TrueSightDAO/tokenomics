/**
 * doGet — invoked by sentiment_importer's trigger_immediate_processing branch
 * for [CURRENCY DEFINITION EVENT] submissions, so processing happens in seconds
 * instead of waiting for the time-driven cron trigger.
 *
 * Edgar config: sentiment_importer/config/application.rb
 *   config.currency_definition_processing_webhook_url
 *
 * Trigger URL shape:
 *   https://script.google.com/macros/s/<deployment>/exec?action=processCurrencyDefinitionsFromTelegramChatLogs
 */
function doGet(e) {
  const action = (e && e.parameter && e.parameter.action) || '';
  Logger.log('doGet called with action: ' + (action || 'none'));

  if (action === 'processCurrencyDefinitionsFromTelegramChatLogs') {
    try {
      Logger.log('Webhook triggered: processCurrencyDefinitionsFromTelegramChatLogs');
      processCurrencyDefinitionsFromTelegramChatLogs();
      Logger.log('Webhook processing completed successfully');
      return ContentService
        .createTextOutput('\u2705 Currency definitions processed successfully. Check execution logs for details.')
        .setMimeType(ContentService.MimeType.TEXT);
    } catch (e) {
      Logger.log('Webhook processing failed: ' + e.message);
      return ContentService
        .createTextOutput('\u274c Error: ' + e.message)
        .setMimeType(ContentService.MimeType.TEXT);
    }
  }

  // Fallback: return help text
  return ContentService
    .createTextOutput(
      'Currency Definition Processor is running.\n' +
      'Available actions:\n' +
      '  ?action=processCurrencyDefinitionsFromTelegramChatLogs  - Process pending currency definitions\n'
    )
    .setMimeType(ContentService.MimeType.TEXT);
}
