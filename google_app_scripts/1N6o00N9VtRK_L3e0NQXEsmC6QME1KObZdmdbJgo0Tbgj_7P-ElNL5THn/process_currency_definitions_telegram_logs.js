/**
 * Scans Telegram Chat Logs for rows with status PROCESSING that contain
 * [CURRENCY DEFINITION EVENT], validates the digital signature, and inserts
 * a row into the Currencies tab.
 *
 * Called by:
 *   1. Edgar webhook: doGet(?action=processCurrencyDefinitionsFromTelegramChatLogs)
 *   2. Time-driven trigger (10-min cron fallback)
 */
function processCurrencyDefinitionsFromTelegramChatLogs() {
  Logger.log('=== processCurrencyDefinitionsFromTelegramChatLogs started ===');

  try {
    const spreadsheet = SpreadsheetApp.openByUrl(TELEGRAM_LOGS_URL);
    const sheet = spreadsheet.getSheetByName(TELEGRAM_LOGS_SHEET);
    if (!sheet) {
      Logger.log('ERROR: Telegram Chat Logs sheet not found');
      return;
    }

    const lastRow = sheet.getLastRow();
    if (lastRow < 2) {
      Logger.log('No data rows in Telegram Chat Logs');
      return;
    }

    // Read all data rows (columns A–J, 0-based indices 0–9)
    const data = sheet.getRange(2, 1, lastRow - 1, 10).getValues();
    let processedCount = 0;
    let skippedCount = 0;

    for (let i = 0; i < data.length; i++) {
      const row = data[i];
      const message = row[TELEGRAM_MESSAGE_COL] ? String(row[TELEGRAM_MESSAGE_COL]) : '';

      // Only process rows with [CURRENCY DEFINITION EVENT]
      if (message.indexOf('[CURRENCY DEFINITION EVENT]') === -1) {
        continue;
      }

      // Only process rows with status PROCESSING
      const status = row[TELEGRAM_STATUS_COL] ? String(row[TELEGRAM_STATUS_COL]).trim() : '';
      if (status !== 'PROCESSING') {
        Logger.log('Skipping row ' + (i + 2) + ': status is ' + status + ' (not PROCESSING)');
        skippedCount++;
        continue;
      }

      const updateId = row[TELEGRAM_UPDATE_ID_COL];
      const messageId = row[TELEGRAM_MESSAGE_ID_COL];
      Logger.log('Processing row ' + (i + 2) + ': update_id=' + updateId + ' message_id=' + messageId);

      // Parse the message
      const details = parseCurrencyDefinitionMessage(message);

      if (!details.currency) {
        Logger.log('Skipping row ' + (i + 2) + ': Could not parse Currency field');
        skippedCount++;
        continue;
      }

      if (!details.digitalSignature) {
        Logger.log('Skipping row ' + (i + 2) + ': No digital signature found');
        skippedCount++;
        continue;
      }

      // Validate digital signature against Contributors Digital Signatures
      const sigResult = findContributorByDigitalSignature(details.digitalSignature);
      if (!sigResult.contributorName) {
        Logger.log('Skipping row ' + (i + 2) + ': ' + sigResult.error);
        skippedCount++;
        continue;
      }

      // Check for duplicates by currency name
      if (currencyDefinitionRecordExists(details.currency)) {
        Logger.log('Skipping row ' + (i + 2) + ': Currency "' + details.currency + '" already exists');
        skippedCount++;
        continue;
      }

      // Insert the record
      const success = insertCurrencyDefinitionRecord(details);
      if (success) {
        processedCount++;
      } else {
        skippedCount++;
      }
    }

    Logger.log('Processing complete: ' + processedCount + ' currency definitions recorded, ' + skippedCount + ' skipped');

    // Sort after inserts so the new SKU is immediately findable
    if (processedCount > 0) {
      sortCurrenciesAZ();
    }
  } catch (e) {
    Logger.log('ERROR in processCurrencyDefinitionsFromTelegramChatLogs: ' + e.message);
  }
}

